package com.example.aiengineer.core.ainn.enums;

public enum NeuralLayerType {
    INPUT,     // Input layer - receives external signals
    HIDDEN,    // Hidden layer - internal processing
    OUTPUT,    // Output layer - produces final results
    MEMORY,    // Memory layer - stores information
    LEARNING,  // Learning layer - adapts and improves
    ATTENTION, // Attention layer - focuses on important signals
    FEEDBACK   // Feedback layer - provides feedback loops
} 